// Extraire les valeurs uniques pour les filtres
export const getUniqueValues = (key: keyof Glasses): string[] => {
  const uniqueValues = new Set<string>();
  
  glasses.forEach(glass => {
    if (typeof glass[key] === 'string') {
      uniqueValues.add(glass[key] as string);
    }
  });
  
  return Array.from(uniqueValues);
};

export const filterOptions = {
  brands: getUniqueValues('brand'),
  styles: getUniqueValues('style'),
  colors: getUniqueValues('color'),
  materials: getUniqueValues('material'),
  genders: getUniqueValues('gender'),
  magazines: Array.from(new Set(glasses.flatMap(glass => 
    glass.magazineFeatures.map(mag => mag.name)
  ))),
  priceRange: {
    min: Math.min(...glasses.map(g => g.price)),
    max: Math.max(...glasses.map(g => g.price))
  }
};